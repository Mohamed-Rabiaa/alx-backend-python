#!/usr/bin/env python3
'''
This module contains the concat function
'''


def concat(str1: str, str2: str) -> str:
    '''
    This function takes two strings and returns the
    concatination of them

    Args:
      st1: The first string
      str2: The second string

    Returns:
      the concatination of the two string
    '''
    return str1 + str2
